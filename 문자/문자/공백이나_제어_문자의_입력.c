// scanf함수로 문자를 입력할 때는 %c 변환 문자를 사용.
// %c는 알파벳이나 숫자 모양의 문자 등 형태가 있는 문자를 입력하지만
// 공백이나 탭 문자, 개행 문자와 같은 제어 문자도 입력하므로 주의해야 함.

#define _CRT_SECURE_NO_WARNINGS

#include <stdio.h>

int main(void)
{
	char ch1, ch2;

	scanf("%c%c", &ch1, &ch2);
	// 2개의 문자를 연속 입력
	printf("[%c%c]", ch1, ch2);
	// 입력된 문자 출력

	printf("%d, %d", ch1, ch2);
	// %d를 사용해서 아스키 코드 값을 출력했을 때
	// 공백 문자는 아스키 코드 값 32, 개행 문자는 10을 출력
	return 0;
}

